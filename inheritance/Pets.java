public class Pets {

    private int age = 0;
    private String name = "";
    private boolean isScary = false;



    public int getAge() {
        return age;
    }
    public String getName() {
        return name;
    }
    public boolean getisScary() {
        return isScary;
    }




    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setIsScary(boolean isScary) {
        this.isScary = isScary;
    }
    
}
